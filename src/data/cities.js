export function projectCity(city) {
  return projectLonLat(city.lat, city.lon);
}

export function projectLonLat(lat, lon) {
  return {
    x: (normalizeLongitude(lon) + 180) / 360,
    y: (90 - lat) / 180,
  };
}

function normalizeLongitude(lon) {
  return ((((lon + 180) % 360) + 360) % 360) - 180;
}
