import { existsSync } from 'node:fs';
import { access, mkdir, readFile, writeFile } from 'node:fs/promises';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const rootDir = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..');
const distDir = path.join(rootDir, 'dist');
const htmlPath = path.join(distDir, 'index.html');
const outPath = path.join(distDir, 'standalone.html');

const MIME_TYPES = {
  '.css': 'text/css',
  '.gif': 'image/gif',
  '.html': 'text/html',
  '.jpg': 'image/jpeg',
  '.jpeg': 'image/jpeg',
  '.js': 'text/javascript',
  '.png': 'image/png',
  '.svg': 'image/svg+xml',
  '.webp': 'image/webp',
};

const assetCache = new Map();

await access(htmlPath);
let html = await readFile(htmlPath, 'utf8');

html = await inlineStylesheets(html);
html = await inlineModuleScripts(html);
html = await inlineFavicon(html);

const generatedNote = '<!-- Generated by scripts/build-standalone.mjs. Edit source files, then run npm run build. -->';
html = html.replace('<!DOCTYPE html>', `<!DOCTYPE html>\n${generatedNote}`);

await mkdir(distDir, { recursive: true });
await writeFile(outPath, html, 'utf8');
console.log(`standalone build: ${path.relative(rootDir, outPath)}`);

async function inlineStylesheets(source) {
  return replaceAsync(source, /<link\b(?=[^>]*rel="stylesheet")(?=[^>]*href="([^"]+)")[^>]*>/g, async (tag, href) => {
    const css = await readTextAsset(href);
    return `<style>\n${await inlineAssetRefs(css)}\n</style>`;
  });
}

async function inlineModuleScripts(source) {
  return replaceAsync(source, /<script\b(?=[^>]*type="module")(?=[^>]*src="([^"]+)")[^>]*><\/script>/g, async (tag, src) => {
    const js = await readTextAsset(src);
    const inlined = collapseDataUrlConstructors(await inlineAssetRefs(js));
    return `<script type="module">\n${inlined.replaceAll('</script', '<\\/script')}\n</script>`;
  });
}

async function inlineFavicon(source) {
  return replaceAsync(source, /<link\b(?=[^>]*rel="icon")(?=[^>]*href="([^"]+)")[^>]*>/g, async (tag, href) => {
    const dataUrl = await assetDataUrl(href);
    return tag.replace(href, dataUrl);
  });
}

async function inlineAssetRefs(source) {
  return replaceAsync(source, /(?:\.\/|\/)?(?:assets\/[^"'`)\\\s]+|favicon\.svg|[\w.-]+\.(?:gif|jpe?g|png|svg|webp))/g, async (assetRef) => {
    return assetDataUrl(assetRef);
  });
}

function collapseDataUrlConstructors(source) {
  return source.replace(/new URL\(("data:[^"]+"),\s*import\.meta\.url\)\.href/g, '$1');
}

async function readTextAsset(assetRef) {
  return readFile(resolveDistAsset(assetRef), 'utf8');
}

async function assetDataUrl(assetRef) {
  const normalized = normalizeAssetRef(assetRef);
  if (assetCache.has(normalized)) return assetCache.get(normalized);
  const filePath = resolveDistAsset(normalized);
  const ext = path.extname(filePath).toLowerCase();
  const mime = MIME_TYPES[ext] || 'application/octet-stream';
  const bytes = await readFile(filePath);
  const dataUrl = `data:${mime};base64,${bytes.toString('base64')}`;
  assetCache.set(normalized, dataUrl);
  return dataUrl;
}

function resolveDistAsset(assetRef) {
  const normalized = normalizeAssetRef(assetRef);
  let filePath = path.normalize(path.join(distDir, normalized));
  if (!existsSync(filePath) && !normalized.includes('/')) {
    filePath = path.normalize(path.join(distDir, 'assets', normalized));
  }
  if (!filePath.startsWith(distDir + path.sep)) {
    throw new Error(`Refusing to inline asset outside dist: ${assetRef}`);
  }
  if (!existsSync(filePath)) {
    throw new Error(`Asset not found: ${assetRef}`);
  }
  return filePath;
}

function normalizeAssetRef(assetRef) {
  return assetRef.replace(/^\.?\//, '');
}

async function replaceAsync(source, pattern, replacer) {
  const matches = [...source.matchAll(pattern)];
  let result = '';
  let lastIndex = 0;
  for (const match of matches) {
    result += source.slice(lastIndex, match.index);
    result += await replacer(...match);
    lastIndex = match.index + match[0].length;
  }
  result += source.slice(lastIndex);
  return result;
}
