import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const sourceFile = resolve(projectRoot, 'data-sources/city-population-wup-2025.json');
const outputFile = resolve(projectRoot, 'src/data/cityPopulationData.generated.js');

const TARGET_YEARS = [1960, 1975, 1980, 1995, 2000, 2020];
const source = JSON.parse(readFileSync(sourceFile, 'utf8'));

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

function roundPopulation(value) {
  return Number(Math.max(0.001, value).toFixed(4));
}

function interpolatePopulation(left, right, year) {
  if (left.population <= 0 || right.population <= 0 || left.year === right.year) {
    const ratio = (year - left.year) / Math.max(1, right.year - left.year);
    return left.population + (right.population - left.population) * ratio;
  }
  const ratio = (year - left.year) / (right.year - left.year);
  return left.population * Math.pow(right.population / left.population, ratio);
}

function boundedEdgeRate(points, atStart) {
  if (points.length < 2) return 0.02;
  const left = atStart ? points[0] : points.at(-2);
  const right = atStart ? points[1] : points.at(-1);
  if (left.population <= 0 || right.population <= 0 || left.year === right.year) return 0.02;
  const raw = Math.pow(right.population / left.population, 1 / (right.year - left.year)) - 1;
  return clamp(raw, -0.03, 0.06);
}

function populationForYear(points, year) {
  const exact = points.find((point) => point.year === year);
  if (exact) return { value: exact.population, quality: 'source' };

  const rightIndex = points.findIndex((point) => point.year > year);
  if (rightIndex > 0) {
    return {
      value: interpolatePopulation(points[rightIndex - 1], points[rightIndex], year),
      quality: 'interpolated',
    };
  }

  if (rightIndex === 0) {
    const rate = boundedEdgeRate(points, true);
    const first = points[0];
    return {
      value: first.population / Math.pow(1 + rate, first.year - year),
      quality: 'derived-backcast',
    };
  }

  const last = points.at(-1);
  const rate = boundedEdgeRate(points, false);
  return {
    value: last.population * Math.pow(1 + rate, year - last.year),
    quality: 'derived-projection',
  };
}

function normalizeRecord(cityId, record) {
  if (!record || typeof record !== 'object') throw new Error(`Missing population record for ${cityId}`);
  const points = Object.entries(record.population || {})
    .map(([year, population]) => ({ year: Number(year), population: Number(population) }))
    .filter((point) => Number.isFinite(point.year) && Number.isFinite(point.population) && point.population > 0)
    .sort((a, b) => a.year - b.year);
  if (points.length === 0) throw new Error(`No usable population observations for ${cityId}`);

  const populationM = {};
  const qualityByYear = {};
  TARGET_YEARS.forEach((year) => {
    const result = populationForYear(points, year);
    populationM[year] = roundPopulation(result.value);
    qualityByYear[year] = result.quality;
  });

  return {
    sourceKey: record.sourceKey || source.defaultSourceKey,
    sourceId: record.sourceId,
    sourceName: record.sourceName,
    country: record.country,
    sourceLat: record.sourceLat,
    sourceLon: record.sourceLon,
    matchDistanceKm: record.distanceKm,
    populationM,
    qualityByYear,
  };
}

const records = Object.fromEntries(
  Object.entries(source.records || {})
    .sort(([a], [b]) => a.localeCompare(b))
    .map(([cityId, record]) => [cityId, normalizeRecord(cityId, record)]),
);

const header = `// This file is generated by scripts/generate-city-data.mjs. Do not edit manually.\n`
  + `// Population is in millions. Source metadata lives in data-sources/city-population-wup-2025.json.\n`;
const body = `${header}\n`
  + `export const CITY_POPULATION_DATA_VERSION = ${JSON.stringify({
    schemaVersion: source.schemaVersion,
    generatedAt: source.generatedAt,
    defaultSourceKey: source.defaultSourceKey,
  }, null, 2)};\n\n`
  + `export const CITY_POPULATION_ANCHOR_YEARS = ${JSON.stringify(TARGET_YEARS)};\n\n`
  + `export const CITY_POPULATION_DATA = ${JSON.stringify(records, null, 2)};\n`;

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(outputFile, body);
console.log(`Wrote ${outputFile}`);
console.log(`Cities: ${Object.keys(records).length}`);
