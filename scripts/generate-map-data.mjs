import { mkdirSync, readFileSync, writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const projectRoot = resolve(__dirname, '..');
const inputDir = resolve(projectRoot, process.argv[2] || 'tmp/natural-earth');
const outputFile = resolve(projectRoot, 'src/data/worldMapPaths.js');

const WIDTH = 1000;
const HEIGHT = 500;
const DECIMALS = 1;

const SOURCES = {
  land: 'https://raw.githubusercontent.com/nvkelso/natural-earth-vector/master/geojson/ne_110m_land.geojson',
  boundary: 'https://raw.githubusercontent.com/nvkelso/natural-earth-vector/master/geojson/ne_110m_admin_0_boundary_lines_land.geojson',
  license: 'Natural Earth public domain data. Made with Natural Earth.',
};

function readGeoJson(fileName) {
  return JSON.parse(readFileSync(resolve(inputDir, fileName), 'utf8'));
}

function round(value) {
  return Number(value.toFixed(DECIMALS));
}

function project([lon, lat]) {
  return [round(((lon + 180) / 360) * WIDTH), round(((90 - lat) / 180) * HEIGHT)];
}

function fmt(value) {
  return Number.isInteger(value) ? String(value) : String(value);
}

function projectedPoints(coords, minDistance = 0.45) {
  const points = [];
  for (const coord of coords) {
    const point = project(coord);
    const prev = points[points.length - 1];
    if (!prev || Math.hypot(point[0] - prev[0], point[1] - prev[1]) >= minDistance) {
      points.push(point);
    }
  }
  return points;
}

function linePath(coords, close = false) {
  const points = projectedPoints(coords);
  if (points.length < 2) return '';
  const commands = points.map(([x, y], index) => `${index === 0 ? 'M' : 'L'}${fmt(x)} ${fmt(y)}`);
  return `${commands.join('')}${close ? 'Z' : ''}`;
}

function polygonPath(rings) {
  return rings.map((ring) => linePath(ring, true)).join('');
}

function pathsFromGeometry(geometry) {
  if (!geometry) return [];
  if (geometry.type === 'Polygon') return [polygonPath(geometry.coordinates)];
  if (geometry.type === 'MultiPolygon') return geometry.coordinates.map((polygon) => polygonPath(polygon));
  if (geometry.type === 'LineString') return [linePath(geometry.coordinates)];
  if (geometry.type === 'MultiLineString') return geometry.coordinates.map((line) => linePath(line));
  return [];
}

function pathsFromFeatureCollection(collection) {
  return collection.features.flatMap((feature) => pathsFromGeometry(feature.geometry)).filter(Boolean);
}

function serializeConst(name, value) {
  return `export const ${name} = ${JSON.stringify(value)};\n`;
}

const land = readGeoJson('ne_110m_land.geojson');
const boundaries = readGeoJson('ne_110m_admin_0_boundary_lines_land.geojson');
const landPath = pathsFromFeatureCollection(land).join('');
const boundaryPath = pathsFromFeatureCollection(boundaries).join('');

mkdirSync(dirname(outputFile), { recursive: true });
writeFileSync(
  outputFile,
  `// Generated by scripts/generate-map-data.mjs from Natural Earth 110m public domain data.\n` +
    `// Source: ${SOURCES.land}\n` +
    `// Source: ${SOURCES.boundary}\n` +
    serializeConst('WORLD_MAP_SOURCE', SOURCES) +
    serializeConst('WORLD_LAND_PATH', landPath) +
    serializeConst('WORLD_BOUNDARY_PATH', boundaryPath),
);

console.log(`Wrote ${outputFile}`);
console.log(`Land path chars: ${landPath.length}`);
console.log(`Boundary path chars: ${boundaryPath.length}`);
